				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="批次" prop="batchNo">
						<el-input v-model="hkshipmentMateriel.batchNo" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="计量单位" prop="unit">
						<el-input v-model="hkshipmentMateriel.unit" :style="inputStyle" ></el-input>
