					</el-form-item>
				</el-col>
			</el-row>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="物料名称" prop="materialName">
						<el-input v-model="hkshipmentMateriel.materialName" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="规格型号" prop="specificationType">
						<el-input v-model="hkshipmentMateriel.specificationType" :style="inputStyle" ></el-input>
