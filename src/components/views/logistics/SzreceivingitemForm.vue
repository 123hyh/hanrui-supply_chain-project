  },
  methods: {
    // 点击保存
    saveForm() {
      var self = this;
      this.$refs.form.validate().then(() => {
        // 表单验证通过后...

        if (!this.isUpdate) {
          // 新增
          appHelper
            .submitCreate("/szreceivingitem/", self.formEntity)
            .then(function() {
              // 提交成功后...
              
              alert("保存成功！");
            });
          this.formEntity = new Szreceivingitem();
          this.loadSzreceivingitemCode();
          this.popup = false;
        } else {
          // 修改
          appHelper
            .submitCreate("/szreceivingitem/", self.formEntity)
            .then(function() {
              // 提交成功后...

              alert("保存成功！");
            });
        }
      });
    },
    //加载深圳收货子表编码
    loadSzreceivingitemCode() {
      appHelper
        .get("/szreceivingitem/itemCode")
        .then(result => (this.formEntity.itemCode = result.data));
    },
    // 点击取消
    closeForm() {
      this.popup = false;
    }
  },

  created() {
    this.loadSzreceivingitemCode();
    // 监听事件
    this.formEvents.$on("openform", () => (this.popup = true));
  }
};
