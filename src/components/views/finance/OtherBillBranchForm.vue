				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="委托单号" prop="entrustOrderNo">
						<el-input v-model="otherBillBranch.entrustOrderNo" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="委托单类型" prop="entrustOrderType">
						<el-select v-model="otherBillBranch.entrustOrderType" :style="inputStyle" ></el-select>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="委托方" prop="clientCode">
						<el-input v-model="otherBillBranch.clientCode" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
