				<el-button type="cancel" @click="closeForm">取消</el-button>
			</div>

			<!-- 表单 -->
			<el-form ref="form" :model="formEntity" label-position="right" :inline="true" :rules="formRules"
			 @submit.native.prevent>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="子表主键" prop="itemCode">
						<el-input v-model="otherBillBranch.itemCode" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="其他计费单编码" prop="billNo">
						<el-input v-model="otherBillBranch.billNo" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
