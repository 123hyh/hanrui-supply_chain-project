						<el-input v-model="delegetebill.delegete" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="受托方" prop="trustee">
						<el-input v-model="delegetebill.trustee" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="往来户名称" prop="dealingName">
						<el-input v-model="delegetebill.dealingName" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="往来户英文名称" prop="dealingNameEn">
						<el-input v-model="delegetebill.dealingNameEn" :style="inputStyle" ></el-input>
