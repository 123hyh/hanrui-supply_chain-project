<template>
	<div class="twoColumnt">

		<el-dialog :visible.sync="popup">
			<!-- 标题 -->
			<div slot="title">{{btnType == 'create'? '新增': '修改'}}认款单子表</div>

			<!-- 按钮行 -->
			<div slot="footer">
				<el-button type="primary" @click="saveForm">确定</el-button>
				<el-button type="cancel" @click="closeForm">取消</el-button>
			</div>

			<!-- 表单 -->
			<el-form ref="form" :model="formEntity" label-position="right" :inline="true" :rules="formRules"
			 @submit.native.prevent>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="子表编码" prop="itemCode">
						<el-input v-model="subscriptionItem.itemCode" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="认款单编码" prop="billNo">
						<el-input v-model="subscriptionItem.billNo" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="跟单人" prop="documentaryPerson">
						<el-input v-model="subscriptionItem.documentaryPerson" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="接单人" prop="receiptPerson">
						<el-input v-model="subscriptionItem.receiptPerson" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
			</el-row>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="委托单号" prop="entrustOrderNo">
						<el-input v-model="subscriptionItem.entrustOrderNo" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="业务类型" prop="bizType">
						<el-select v-model="subscriptionItem.bizType" :style="inputStyle" ></el-select>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="报关类型" prop="declareCustoms">
						<el-select v-model="subscriptionItem.declareCustoms" :style="inputStyle" ></el-select>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="计费结算单号" prop="feeNo">
						<el-input v-model="subscriptionItem.feeNo" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
			</el-row>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="计费项目" prop="feeProject">
						<el-input v-model="subscriptionItem.feeProject" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="结算金额" prop="settlementAmount">
						<el-input v-model="subscriptionItem.settlementAmount" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="已认款金额" prop="hasSubscribedAmount">
						<el-input v-model="subscriptionItem.hasSubscribedAmount" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="认款金额" prop="subscribedAmount">
						<el-input v-model="subscriptionItem.subscribedAmount" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
			</el-row>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="结算单币别" prop="singleCurrency">
						<el-select v-model="subscriptionItem.singleCurrency" :style="inputStyle" ></el-select>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="立账发生时间" prop="setUpTime">
						<el-date-picker value-format="yyyy-MM-dd HH:mm:ss" type="date" v-model="subscriptionItem.setUpTime" :style="inputStyle"></el-date-picker>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="应收日期" prop="receivablesDate">
						<el-date-picker value-format="yyyy-MM-dd HH:mm:ss" type="date" v-model="subscriptionItem.receivablesDate" :style="inputStyle"></el-date-picker>
					</el-form-item>
				</el-col>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="收款时间" prop="collectionDate">
						<el-input v-model="subscriptionItem.collectionDate" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
			</el-row>
			<el-row>
				<el-col :span="6">
					<el-form-item :label-width="labelWidth" label="备注" prop="remark">
						<el-input v-model="subscriptionItem.remark" :style="inputStyle" ></el-input>
					</el-form-item>
				</el-col>
			</el-row>

			</el-form>
		</el-dialog>
	</div>
